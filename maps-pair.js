const fs = require('fs');

function removeDuplicates(strings) {
    console.log('strings: ', strings.length);
    const uniqueStrings = new Set(strings);
    console.log('strings: ', strings.length);
    return Array.from(uniqueStrings);
}

function saveToFileAsJS(array, filename, variableName) {
    // Создаем строку, которая определяет массив как экспортируемую переменную
    const data = `const ${variableName} = ${JSON.stringify(array, null, 2)};\nmodule.exports = { ${variableName} };`;
    fs.writeFile(filename, data, (err) => {
        if (err) {
            console.error('Не удалось сохранить файл:', err);
        } else {
            console.log(`Файл ${filename} успешно сохранен.`);
        }
    });
}

function sortAndSave(strings) {
    // Фильтруем и сортируем строки, где 'fiat' (начинается с 'CASH') переходит в 'currency'
    const fiatToCurrency = strings.filter(str => str.startsWith('CASH')).sort();

    // Фильтруем и сортируем строки, где 'currency' переходит в 'fiat' (заканчивается на 'CASH')
    const currencyToFiat = strings.filter(str => str.includes('-to-CASH')).sort();
  
    // Сохраняем оба массива в разные файлы в формате JavaScript
    saveToFileAsJS(fiatToCurrency, 'fiatToCurrency.js', 'fiatToCurrency');
    saveToFileAsJS(currencyToFiat, 'currencyToFiat.js', 'currencyToFiat');
}

